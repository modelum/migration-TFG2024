CREATE TABLE User
	address BOOLEAN,
	phone NUMERIC,
	id NUMERIC
	PRIMARY KEY (id)
CREATE TABLE Shop
	address VARCHAR(255),
	id 
	PRIMARY KEY (id)
CREATE TABLE Order
	orderNumber, 
	date, 
	user, 
	shop 
	CONSTRAINT User_fk FOREIGN KEY (user) REFERENCES User (id) ON DELETE NO_ACTION ON UPDATE CASCADE,
	CONSTRAINT Shop_fk FOREIGN KEY (shop) REFERENCES Shop (id) ON DELETE NO_ACTION ON UPDATE CASCADE
CREATE TABLE Product
	price 
CREATE TABLE OrderDetails
	quantity, 
	order 
	CONSTRAINT Order_fk FOREIGN KEY (order) REFERENCES User (id) ON DELETE NO_ACTION ON UPDATE CASCADE
CREATE TABLE producto
	id NUMERIC NOT NULL DEFAULT 1,
	productName 
	PRIMARY KEY (id)
	CONSTRAINT OrderDetails_fk FOREIGN KEY () REFERENCES User (id) ON DELETE NO_ACTION ON UPDATE CASCADE
